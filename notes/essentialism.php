<h1>
	Essentialism
</h1>

<p>Essentialism, while not a deeply philosophical read, is certainly a reaction to the problems of our time. In an age where people to struggle to find meaning in a world of "busy", Essentialism is a philosophy to escape the bullshit.</p><br>

</div>

<div class="notes">

	<div><span style="font-size: 24px;"><b>Essence</b></span></div>
	<div><b><span style="font-size: 18px;"><br/></span></b></div>
	<div><img src="Essentialism.resources/709EE798-96AD-4CC6-A642-170239064B60.png"/></div>
	<div><b><span style="font-size: 18px;"><br/></span></b></div>
	<div><u>1. The Essentialist</u></div>
	<ol>
	<li><b>Value Proposition:</b> Once you give yourself permission to stop trying to do it all, to stop saying yes to everyone, you can make your highest contribution toward the things that really matter.</li>
	<li><b>Less but Better.</b> Pursue this in a disciplined way.</li>
	<li>Am I investing in the right activities? Making the wisest possible investment of your time and energy. <b>If you don’t prioritize your life, someone else will.</b></li>
	<li>Success arises from Clarity of purpose, but then tugs away from it through distraction.</li>
	<li><b>Modern day issues:</b> Overabundance of choice bringing about decision fatigue, social pressure/connectedness to do more, the idea that you can/have to do it all now, </li>
	<li><span style="color: rgb(255, 38, 0);"><b>Methods for elimination:</b></span>
	<ol>
	<li><span style="color: rgb(255, 38, 0);"><b>Explore and evaluate</b>: Do I love this?, Do I do/use it often?, Does this make the highest contribution to my goal?</span></li>
	<li><span style="color: rgb(255, 38, 0);"><b>Eliminate: </b>If I did not already own this/wasn’t pre-committed, how much would I pay/would I do this?</span></li>
	<li><span style="color: rgb(255, 38, 0);"><b>Execute:</b> Need a systematic method to ensure it gets done.</span></li>
	</ol>
	</li>
	<li>There is a huge tradeoff between a lot of good things and a few great. </li>
	<li><b>Core-Mindset of an essentialist:</b>
	<ol>
	<li><b>Individual Choice:</b> I can choose how to spend my energy and time.</li>
	<li><b>The prevalence of noise:</b> Almost everything is noise, and a very few things are exceptionally valuable.</li>
	<li> <b>The Reality of tradeoffs:</b> We can’t have or do it all.</li>
	</ol>
	</li>
	<li><b>Essentialist explore everything.</b> Seemingly paradoxical, you do so to evaluate options before committing.</li>
	<li><b>Highest contribution:</b> What am I inspired by? What am I talented at? What meets a significant need in the world? Looking to do the <i>right thing, in the right way, at the right time.<br/>
	<br/></i></li>
	</ol>
	<div><u>2. Choose: The Invincible Power of Choice</u></div>
	<ol>
	<li><b>Choice is not a thing, it is an <i>action</i>.</b> The ability to choose cannot be taken away, it can only be forgotten.</li>
	<li> <b>Replace “I have to” with “I choose to”.</b> “My first act of free will shall be to believe in free will”.<br/>
	<br/></li>
	</ol>
	<div><u>3. Discern: The Unimportance of Practically Everything</u></div>
	<ol>
	<li>Certain types of effort yield higher results.</li>
	<li><b>Pareto Principal:</b> Focusing on building/improving key features will yield best results. 20% of your efforts drive 80% of your results.</li>
	<li><b>There are exponential differences in results for certain things.</b> 10,000X difference (explains tremendous success vs. nobody).</li>
	<li><b>Essentialists think almost everything is non-essential.</b> They distinguish the <i>vital few</i> from the <i>trivial many.</i><br/>
	<br/></li>
	</ol>
	<div><u>4. Trade-off: Which Problem Do I want?</u></div>
	<ol>
	<li><b>Straddling:</b> Keeping your current strategy intact while simultaneously trying to adopt another.</li>
	<li>Trade-offs are real. We cannot have it all.</li>
	<li><span style="color: rgb(255, 38, 0);"><b>Essentialists don’t ask "How Can I Do Both"? They ask<br/>
	"Which problem do I want"? </b></span></li>
	<li><font color="#FF2600"><b>It is not “What do I have to Give Up?” but “What do I want go Big on?”</b>. Trade-offs are just part of life.</font></li>
	</ol>
	<div><span style="color: rgb(255, 38, 0);"><br/></span></div>
	<div><span style="color: rgb(255, 38, 0);"><br/></span></div>
	<div><span style="color: rgb(255, 38, 0);"><br/></span></div>
	<div><span style="font-size: 24px;"><b>Evaluate</b></span></div>
	<div><b><span style="font-size: 24px;"><br/></span></b></div>
	<div><u>5. Escape: The Perks of Being Unavailable</u></div>
	<ol>
	<li>Don’t get so busy doing things that you can’t even determine if they’re worthwhile.</li>
	<li><b>Create space to explore life</b>.</li>
	<li>Focus is something we do. <b>Like eyesight, focus is not on one thing, but adjusting and adapting to our field of vision.</b></li>
	<li><span style="color: rgb(255, 38, 0);">The busier we are, the more we need to take time to think and meditate.</span></li>
	<li>Bill Gates takes two weeks off to read and think  at the busiest parts of the year <br/>
	<br/></li>
	</ol>
	<div><u>6. Look: See What Really Matters</u></div>
	<ol>
	<li>Take the Who, What, Where, When, How, and Why, and answer <b>What is the point?</b></li>
	<li><b>Pay attention to the signal in the noise</b>. Hear what is <i>not</i> being said. Scan to find the essence of the information.</li>
	<li><b>Keep a journal.</b> Review entries every 90 days or so.</li>
	<li><b style="color: rgb(255, 38, 0);">Get out into the field</b><font color="#FF2600">. Talk to people. Find the essence of what matters.</font></li>
	<li><b><span style="color: rgb(255, 38, 0);">Look for unusual/abnormal details.</span></b></li>
	<li><b><span style="color: rgb(255, 38, 0);">Clarify the question.<br/>
	<br/></span></b></li>
	</ol>
	<div><u>7. Play: Embrace the Wisdom of Your Inner Child</u></div>
	<ol>
	<li>School is derived from greek “skole” meaning <i>leisure</i>.</li>
	<li><b>Play is essential and sparks creativity and innovation</b>. </li>
	<li><b>Play broadens perspectives and adds options. It cures stress. It stimulates both organized and creative parts of the brain.</b> So go play.<b><br/>
	<br/></b></li>
	</ol>
	<div><u>8. Sleep: Protect the Asset</u></div>
	<ol>
	<li><b>Protect your mind, body, and spirit.</b> Pace yourself, nurture yourself, and give yourself fuel to thrive.</li>
	<li><b>Sleep.</b> is a priority, breeds creativity, is for high performers, enables productivity and high level of mental contribution.</li>
	<li>A nap can help with creativity and decision making.<br/>
	<br/></li>
	</ol>
	<div><u>9. Select: The Power of Extreme Criteria</u></div>
	<ol>
	<li>The answer is either “HELL YEA” or “No”.</li>
	<li><span style="color: rgb(255, 38, 0);"><b>Rate choices from 0 to 100. Drop anything below a 90.</b> Shifts to logical evaluation over emotional.</span></li>
	<li><b>Make decisions by design, not default.</b>Use narrow criteria like “Is this <i>exactly</i> what I’m looking for?"</li>
	<li><b>Method for selection:</b> Write down the opportunity being offered. Write down your three minimum criteria for accepting. Write down three ideal criteria for accepting. <i>The opportunity must possess all of the the first three and two of the second.</i></li>
	</ol>
	<div><i><br/></i></div>
	<div><i><br/></i></div>
	<div><span style="font-size: 24px;"><b>Eliminate</b></span></div>
	<div><i>If I didn’t have this opportunity, what would I be willing to do to acquire it?</i></div>
	<div><br/></div>
	<div><u>10. Clarify: One Decision that Makes a Thousand</u></div>
	<ol>
	<li><b>Clarity of purpose consistently predicts success.</b> Lack of clarity creates confusion, stress, and frustration. </li>
	<li>Lack of clarity leads to playing politics and teams functioning poorly. </li>
	<li><b>Pursuing multiple different paths well does not necessarily sum to a meaningful whole. </b></li>
	<li><b>Mission:</b> General and inspirational. Shoot for <b>Essential Intent:</b> concrete and general. Meaningful and measurable</li>
	<li><b>Make one decision that eliminates one thousand</b></li>
	<li><b>Crafting:</b> ask "if we could truly be excellent at only one thing, what would it be?" and "how will we know when we're done?" </li>
	</ol>
	<div><br/></div>
	<div><u>11. Dare: The power of a graceful no</u></div>
	<ol>
	<li>A well done no can change the world— Rosa parks. </li>
	<li><b>Lack of clarity makes it unclear what we should say yes or no to. </b></li>
	<li><b>We say yes to avoid social conflict. </b></li>
	<li><span style="color: rgb(255, 38, 0);"><b>How to say no:</b> separate decisions from relationships, choose no but don't say it (I'm flattered, but I'm afraid I'm overcommitted). Focus on the trade-off. Be aware everyone is selling something.  No involves trading popularity for respect. </span></li>
	<li><b>A clear no can be more graceful than a vague/noncommittal yes. </b></li>
	<li><span style="color: rgb(255, 38, 0);"><b>Tactics for saying no:</b></span>
	<ol>
	<li><span style="color: rgb(255, 38, 0);">take a three second pause to consider (the awkward pause)</span></li>
	<li><span style="color: rgb(255, 38, 0);">say it politely/delay (busy now, but maybe when I'm freed up)</span></li>
	<li><span style="color: rgb(255, 38, 0);">default to "let me check my calendar"</span></li>
	<li><span style="color: rgb(255, 38, 0);">have an e-mail auto responder when busy</span></li>
	<li><span style="color: rgb(255, 38, 0);">inform a superior of how it would disrupt other work</span></li>
	<li><span style="color: rgb(255, 38, 0);">use humor</span></li>
	<li><span style="color: rgb(255, 38, 0);">clearly state "you're welcome to do this, but I will do this" as a means of outlining what you won't do.</span></li>
	<li><span style="color: rgb(255, 38, 0);">I can't do it but X might be able to<br/>
	<br/></span></li>
	</ol>
	</li>
	</ol>
	<div><u>12. Uncommit: Win Big By Cutting Your Losses</u></div>
	<ol>
	<li><b>Avoid Sunk-Cost Bias. </b></li>
	<li>Essentials are comfortable with cutting losses. Ask “If I weren’t already invested, how much would I invest now?” and “What else could I do with this time and money?"</li>
	<li><b>When you own a project/item, it is harder to let go.</b> Fixes:
	<ol>
	<li>Ask, if I didn’t own it, how much would I pay? If I didn’t already have this obligation, would I take it?</li>
	<li>Let go of the fear of waste.</li>
	<li><b>Admit failure to begin success.</b></li>
	<li>Get a neutral second opinion.</li>
	<li>Avoid <b>Status Quo bias:</b> The tendency to continue doing something just because we always have done it.</li>
	</ol>
	</li>
	<li><b>Use Zero-Based budgeting:</b> Don’t budget time/energy based on what you’ve previously been doing, start from scratch and justify everything.</li>
	<li><b>Run a Reverse Pilot:</b> Ask if anything negative will arise from letting go.<br/>
	<br/></li>
	</ol>
	<div><u>13. Edit: The Invisible Art</u></div>
	<ol>
	<li><b>Good editors make it hard <i>not</i> to see what’s important.</b></li>
	<li>Use <b>Deliberate Subtraction</b> to add life and energy.</li>
	<li>Eliminated distraction to add value.</li>
	<li>How:
	<ol>
	<li><b>Cut-out options</b>. Not activities, options for activities. There are things you just don’t do.</li>
	<li><b>Condense:</b> Lower the ratio of effort to results. Replace the meaningless with meaningful.</li>
	<li><b>Correct:</b> Relate all activities back to your core cause.</li>
	<li><b>Edit Less:</b> Edit less. get involved less. Allow more to happen before we jump in.<br/>
	<br/></li>
	</ol>
	</li>
	</ol>
	<div><u>14. The Freedom of Setting Boundaries</u></div>
	<ol>
	<li><b>Boundaries proactively eliminate the demands and encumbrances from others that distract us. </b></li>
	<li><span style="color: rgb(255, 38, 0);"><b>Essentialists:</b> know that which limits you to become limitless. See boundaries as liberating. Set rules that eliminate the need for no. </span></li>
	<li><b>Help people find solutions, but don't rob them of their problems.</b> Build fences ahead of time. Think fence around a playground. Clear boundaries allow for us to explore all options within them.</li>
	<li><b>Know your dealbreakers.</b> Write down every time you feel a boundary is crossed. This helps <b>delineate your personal boundaries.</b> Craft social contracts. <br/>
	<br/></li>
	</ol>
	<div><br/></div>
	<div><span style="font-size: 24px;"><b>Execute.</b></span></div>
	<div><i>Doing the essential things. </i></div>
	<div><i><br/></i></div>
	<div><u>15. buffer: the unfair advantage</u></div>
	<ol>
	<li><b>Buffer:</b> something which prevents two things from coming in contact and harming each other. </li>
	<li><b>Build buffers. Practice extreme and early preparation. </b></li>
	<li>Use good times to creat a buffer for the bad. How?</li>
	<li><span style="color: rgb(255, 38, 0);">Prepare for the worst. Add 50% to your time estimate. People admit having a tendency to underestimate while believing current estimates are correct. </span></li>
	<li><span style="color: rgb(255, 38, 0);">Ask: what risks do you face? What is the worst-case scenario? What are the social implications? Financial? How can you invest to reduce risks. <br/>
	<br/></span></li>
	</ol>
	<div>
	<div><u>16. Subtract: bring forth more by removing obstacles</u></div>
	</div>
	<ol>
	<li><b>Find the biggest thing slowing you down and alleviate it. Get better/lighten the load. </b></li>
	<li><b>Be clear on your essential intent.</b> Can't know what to remove until we know what we want. </li>
	<li><b>Identify the slowest hiker.</b> What obstacle, if removed, would make most others disappear? <b>If possible, remove the obstacle. <br/>
	<br/></b></li>
	</ol>
	<div><u>17. Progress: The power of small wins</u></div>
	<ol>
	<li><b>Essentialists:</b> Starts small and get big results. Celebrates small acts of progress.</li>
	<li><b>Minimum Viable Progress:</b> What is the smallest bit I can do that will be valuable to the task?</li>
	<li><b>M</b><b>inimum Viable Preparation:</b> Start preparing far ahead of time by jotting down ideas, establishing outlines/frameworks, or dictating key points.</li>
	<li>Visualize your progress.<br/>
	<br/></li>
	</ol>
	<div><u>18. Flow: The Genius of Routine</u></div>
	<ol>
	<li><b>Make essential the default position. Create a routine that enshrines the essential, making execution effortless.</b></li>
	<li><b>To change bad habits:</b> Identify cue’s, associate with something essential.</li>
	<li><b>Create new triggers:</b> Develop a cue to indicate you should do something. Every time you do the habit gets stronger.</li>
	<li><b>Focus on the hardest thing first.</b> </li>
	<li>Mix up your routines: Different routines for different days.<br/>
	<br/></li>
	</ol>
	<div><u>19. Focus: What’s important now?</u></div>
	<ol>
	<li><b>Stay focused on yourself in the now.</b> There is a difference between losing and being beaten.</li>
	<li><b>Essentialists:</b> Live in the moment, focus on what is important now, enjoy the present</li>
	<li><b>We can only focus on one thing at a time. Don’t multitask.</b> When people are on their phones and you’re speaking, pause until they’re done.</li>
	<li><span style="color: rgb(255, 38, 0);"> How to be present:</span>
	<ol>
	<li><span style="color: rgb(255, 38, 0);"><b>When feeling stressed, stop, breathe, and take time what needs to be done right now.</b></span></li>
	<li><span style="color: rgb(255, 38, 0);"><b>Write down all the ideas buzzing around in your head.<br/>
	<br/></b></span></li>
	</ol>
	</li>
	</ol>
	<div><u>20. Be: The Essential Life</u></div>
	<ol>
	<li>Focus on less achievements, but better, bigger. More powerful.</li>
	<li><b>Focus on opportunities which bring you clarity. Ignore others which contribute to the haze.</b></li>
	<li>Gives you the opportunity to experience more joy, to live life more fully.</li>
	<li><b>Whenever making a decision ask: What is essential?</b> In the end, everything else fades away.</li>
	<li><b>Clarity=Success</b></li>
	</ol>
