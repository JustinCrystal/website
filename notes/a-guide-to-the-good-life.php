<h1>
	A Guide to the Good Life
</h1>

<p>A Guide to the Good Life is a manifesto on the often misunderstood philosophy of Stoicism. Often associated with the abandonment of emotion, Stoicism is actually focused on minimizing negative emotion and maximizing your propensity for tranquility and joy. The whole thing reads like a practical manual for living Stoicism, with <i>just</i> enough philosophical backbone.</p><br>

</div>


<div class="notes">
	<div>Don’t ask yourself what you want <i>in</i> life, but <i>What do you want to get out of it?</i> This allows for a coherent philosophy of life; to pursue something valuable rather than to get caught up in the distractions of living.</div>
	<div><br/></div>
	<div><b>Philosophy of Life:</b> A goal and strategy for obtaining what you want to get out of life. </div>
	<div><b><br/></b></div>
	<div><b><i>Why Stoicism?</i></b> It has a strong psychological component to it, it’s inherently human. The goal of stoicism is <i><b>Tranquility:</b> The banishment of negative emotions and the emergence of joy.</i></div>
	<div><i><br/></i></div>
	<div>While religion provides a framework for being a <i>good person</i> and having a <i>good afterlife,</i> it can fall short on having a <i>good life</i>. </div>
	<div><br/></div>
	<div>Philosophy combines the <u>practical</u> (philosophy of life, “art of life”) with the <u>theoretical</u> (logical, physical, ethical “Having a good spirit”).</div>
	<div><br/></div>
	<div>It focuses on what is <b>virtuous:</b> what we were designed to do, or excellent.</div>
	<div><br/></div>
	<div>We were given reason an we must use it, most don’t. We are social creatures, we have duties to our fellow men.</div>
	<div><br/></div>
	<div><br/></div>
	<div><u><span style="font-size: 24px;"><b>Stoic Psychological Techniques</b></span></u></div>
	<div><b><span style="font-size: 24px;"><br/></span></b></div>
	<div><span style="font-size: 18px;"><b>Negative Visualization:</b> <i>What’s the worst that can happen?</i></span></div>
	<div><i><br/></i></div>
	<div><b>Hedonistic Adaptation:</b> Fulfilling material desires only temporarily increases happiness. The answer is to <i>desire that which you already have.</i></div>
	<div><i><br/></i></div>
	<ul>
	<li>Understand you can lose something. 
	<ul>
	<li>This makes you appreciate that thing more in the moment</li>
	<li>It prepares you for it’s inevitable loss</li>
	</ul>
	</li>
	<li>Imagine the deaths of your loved ones and yourself. “<i>It <u>is</u> wonderful to be alive!"</i></li>
	<li>Imagine bad things that could happen to you and others...
	<ul>
	<li>But understand there’s a difference between contemplation and worry. Don’t worry, but contemplate so you develop an appreciation for those things.</li>
	</ul>
	</li>
	</ul>
	<div><br/></div>
	<div><br/></div>
	<div><span style="font-size: 18px;"><b>The Dichotomy of Control:</b> <i>Concern yourself with the internal</i></span></div>
	<div><i><span style="font-size: 18px;"><br/></span></i></div>
	<div>Want only what you can obtain, only what is up to you  (opinions and actions)</div>
	<ul>
	<li><i>Complete Control:</i> Goals, Values</li>
	<li>Set internal goals that you have <u>full</u> control over:
	<ul>
	<li>Do as well as possible- still causally connected to achieving a results-oriented outcome- reduces anguish while maintaining outcomes</li>
	</ul>
	</li>
	</ul>
	<div><br/></div>
	<div><br/></div>
	<div><b style="font-size: 18px;">Fatalism:</b><span style="font-size: 18px;"> </span><i><span style="font-size: 18px;">Letting go of the past… and present</span></i></div>
	<div><i><span style="font-size: 18px;"><br/></span></i></div>
	<ul>
	<li>Accept that the past has happened- use it to learn and no more.</li>
	<li>This includes all events that have led to now. We can’t change what has led to this moment, only things going forward</li>
	<li><b>Be content with what you have, but strive for better.</b></li>
	</ul>
	<div><b><br/></b></div>
	<div><b><br/></b></div>
	<div><span style="font-size: 18px;"><b>Self-Denial:</b> <i>On the Dark side of Pleasure</i></span></div>
	<div><i><span style="font-size: 18px;"><br/></span></i></div>
	<ul>
	<li>We should sometime alive as if bad things have happened to us- practice <i>poverty, hunger, and thirst</i>. </li>
	<li><b>Benefits:</b>
	<ul>
	<li>Harden ourselves against misfortune</li>
	<li>Grow confidence that we can handle discomfort, therefore reducing anxiety</li>
	<li>Further builds appreciation for what we already have</li>
	<li>Widens comfort zone and increases joy!</li>
	</ul>
	</li>
	<li><b>*Build self-control and discipline like a muscle*</b></li>
	<li><b>Abstain from some pleasures:</b>
	<ol>
	<li>Those that capture us in a single use (meth, cocaine)</li>
	<li>Pass up harmless pleasure to practice self-control- there is a unique pleasure in doing so</li>
	<li>Do nothing sheerly for the sake of pleasure- it is not a good</li>
	</ol>
	</li>
	</ul>
	<div><br/></div>
	<div><br/></div>
	<div><span style="font-size: 18px;"><b>Mediation</b></span></div>
	<div><b><span style="font-size: 18px;"><br/></span></b></div>
	<div>Contemplate:</div>
	<ul>
	<li>Daily happenings,</li>
	<li>Areas for improvement</li>
	<li>Where you felt upset</li>
	<li>Assess why’s- are you practicing stoic techniques?</li>
	</ul>
	<div><br/></div>
	<div>Play the part of participant and spectator- have a second you who sits back and observes your motivations and whys.</div>
	<div><br/></div>
	<div><br/></div>
	<div><br/></div>
	<div><u><span style="font-size: 24px;"><b>Stoic Advice</b></span></u></div>
	<div><b><span style="font-size: 24px;"><br/></span></b></div>
	<div><span style="font-size: 18px;"><b>Duty, On loving mankind</b></span></div>
	<div><b><span style="font-size: 18px;"><br/></span></b></div>
	<div>Relationships are responsible for our highest highs and lowest lows. <u>Primary Function:</u></div>
	<ol>
	<li>To reason</li>
	<li>Fellowship, mutual collaboration toward happiness</li>
	</ol>
	<div><br/></div>
	<div><br/></div>
	<div><span style="font-size: 18px;"><b>Social Relations, on Dealing with People</b> (<i>Vices are contagious)</i></span></div>
	<div><i><span style="font-size: 18px;"><br/></span></i></div>
	<ul>
	<li>Befriend those who share your values and are even better about them than you</li>
	<li>Stay silent in conversations that stray away from your values, or divert the conversation</li>
	<li><u>In Handling annoying people:</u>
	<ul>
	<li>Reflect on your own shortcomings— Increases empathy</li>
	<li>Understand that your annoyance is more detrimental than their behavior</li>
	<li>People don’t control their bad tendencies (fully) —&gt; Put it in its universal context.</li>
	</ul>
	</li>
	</ul>
	<div><br/></div>
	<div><br/></div>
	<div><b><span style="font-size: 18px;">Handling Insults</span></b></div>
	<div><b><span style="font-size: 18px;"><br/></span></b></div>
	<ul>
	<li>Pause to considers if it’s true— if yes, fix it! If no, who cares?</li>
	<li>If the source is credible, learn form it.</li>
	<li>Consider the insulters knowledge— Should you pity them for their childishness?</li>
	<li>If you dislike the insulter and they disapprove of you- then great!</li>
	<li><b>respond to insults with humor</b>(self-deprecating), or don’t respond at all</li>
	</ul>
	<div><br/></div>
	<div><br/></div>
	<div><span style="font-size: 18px;"><b>Grief:</b> <i>Defeating Tears w/Reason</i></span></div>
	<div><i><span style="font-size: 18px;"><br/></span></i></div>
	<ul>
	<li>Retrospective Negastive Visualization: Joy at having had it at all</li>
	<li>Do not catch others’ grief.</li>
	</ul>
	<div><br/></div>
	<div><br/></div>
	<div><span style="font-size: 18px;"><b>Anger</b></span></div>
	<div><b><span style="font-size: 18px;"><br/></span></b></div>
	<ul>
	<li>Remember a person may have good intentions </li>
	<li>Your anger is far more hurtful than whatever happened</li>
	<li>Remember that you anger people all the time</li>
	<li><b>Find angering things amusing rather than tragic</b>- consider the cosmic significance</li>
	<li><b>Physiology:</b> Relax your face. Slowdown. Speak quietly.</li>
	</ul>
	<div><b><span style="font-size: 18px;"><br/></span></b></div>
	<div><b><span style="font-size: 18px;"><br/></span></b></div>
	<div><span style="font-size: 18px;"><b>Seeking Fame, Luxurious Living</b></span></div>
	<div><b><span style="font-size: 18px;"><br/></span></b></div>
	<ul>
	<li>Fame enslaves us to others and getting them to like us</li>
	<li>Think if someones values align with your before caring if they like you</li>
	<li>Living to luxuriously risks you becoming hard to please, lowers happiness</li>
	<li>Our values determine our happiness</li>
	</ul>
	<div><br/></div>
	<div><br/></div>
	<div><span style="font-size: 18px;"><b>Old Age and Death</b></span></div>
	<div><b><span style="font-size: 18px;"><br/></span></b></div>
	<ul>
	<li>Encountering death gives us a new appreciation for life— our desires no longer rule us</li>
	<li>Death is distorted by the prospect of dying— you have nothing to fear if you have lived a good life, after life or not</li>
	</ul>
	<div><b><span style="font-size: 18px;"><br/></span></b></div>
	<div><b><span style="font-size: 18px;"><br/></span></b></div>
	<div><b><span style="font-size: 18px;">On Becoming Stoic</span></b></div>
	<div><b><span style="font-size: 18px;"><br/></span></b></div>
	<ul>
	<li>Know that having values often makes people feel you’re judging theirs.</li>
	<li>Enjoy things which can’t be taken away,  enjoy things  w/o feeling entitled to them, don’t become a connoisseur (in the worst sense of the word)</li>
	</ul>
	<div><br/></div>
	<div><b>Stoicism reduces negative emotions and increases the chance of delight in what we have</b>.</div>
	<div><br/></div>
	<div><br/></div>
	<div><u><b><span style="font-size: 24px;">Stoicism for Modern Lives</span></b></u></div>
	<div><u><b><span style="font-size: 24px;"><br/></span></b></u></div>
	<ul>
	<li>There is at best a loose connection between our external circumstances and our happiness</li>
	<li>Become self-aware, use reasoning to defeat negative emotions and desires</li>
	<li>Use reason to override your evolutionary programming</li>
	<li>Put yourself in situation which make you uncomfortable- builds self-discipline, expands your comfort zone, and creates an appreciation for what you have</li>
	</ul>
