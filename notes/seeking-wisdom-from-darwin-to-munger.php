	<div><span style="color: rgb(255, 38, 0);"><span style="font-size: 18px;"><b>Guidelines to Better Thinking</b></span></span></div>
	<div><b><span style="font-size: 18px;"><span style="color: rgb(255, 38, 0);"><br/></span></span></b></div>
	<ol>
	<li><b>Models of Reality</b>
	<ul>
	<li>Ask yourself: <b>Is there anything I can do to make my whole life and my whole mental process work better?</b> Models help understand the “why” and predicting “how"</li>
	<li>What characterizes a useful Model?
	<ul>
	<li>A model agreeing with reality. Ex. from biology “<i>People on average act out of self-interest</i>.”</li>
	<li>Another, autocatalysis, a process which, once started, speeds up on its own. In doing A, you get B and C. (Disney had movies in cans, could easily move to VHS and DVD).</li>
	<li><u>Models from hard science and engineering are the most reliable.</u> Engineering quality, backup systems, breakpoint, and critical mass</li>
	</ul>
	</li>
	<li>Considering many ideas achieves a holistic view
	<ul>
	<li>In most big problems, you must use all of the big disciplines- math, physic, chemistry, engineering, biology, psychology</li>
	<li>Always stick to the fundamental- “I’m using physics” “I’m using biology"</li>
	<li>Then understand how these things relate and combine. Additive? Multiplicative? Exponential? Critical mass point?</li>
	<li>Try to find connections between ideas, translate them into different disciplines.</li>
	<li>Combine ideas- <u>business value comes from scaling in math, systems and constraints from physics, and competitive advantage from microeconomics</u></li>
	</ul>
	</li>
	<li><b>How can we learn an idea so it sticks in our memory?</b>
	<ul>
	<li>Boil it down into simple but powerful sentences containing the essence of an idea</li>
	<li>Give it a hook- a dramatic/emotional story that is easily remembered</li>
	</ul>
	</li>
	<li>Search for explanation
	<ul>
	<li>Regularly ask why things happen- but everyday things. Why does food cook the way it does? Why does physics/biology happen how it does? Why do people do what they do?</li>
	<li><b>Newton’s Law:</b> 1. An object in motion stays in motion, and an object at rest stays at rest (without intervention of an outside force). 2. Force is the product of mass and acceleration 3. Force works in pairs. Object exerting force on another has it exerted back</li>
	<li>Assume these are true until proven otherwise<br/>
	<br/></li>
	</ul>
	</li>
	</ul>
	</li>
	<li><b>Meaning</b>
	<ul>
	<li>Knowledge is only valuable if it’s useful and we understand what it means. (Names are meaningless, they change depending on what language you speak! Understanding is what counts)</li>
	<li>Try to explain things without fancy/technical jargon. <b>True understanding can be demonstrated using laymen terms.</b></li>
	<li>Understanding meaning, requires you ask some basic questions:
	<ul>
	<li><i>Words: </i>What do the words mean and imply? Can we translate them into an ordinary situation that means something?</li>
	<li><i>Events:</i> What effect is produced? What is really happening? What is it doing? What is accomplished?  Under what conditions does it happen? What else does it mean?</li>
	<li><i>Causes:</i> What is happening and why? Is this working? Why or why not? Why does it work here but not there? How can and what makes it happen? What are the mechanisms?</li>
	<li><i>Implications:</i> What is the consequence of this observation, event or experience? What does that imply?</li>
	<li><i>Purpose:</i> Why should we do that? Why do I want this to happen?</li>
	<li><i>Reason:</i> Why is this better than that?</li>
	<li><i>Usefulness:</i> What is the applicability of this? Does it mean anything in relation to what I want to achieve?</li>
	</ul>
	</li>
	<li><b>If you can’t explain it simply, you don’t understand it well enough</b></li>
	<li>Ask “What happens?"
	<ul>
	<li>For investing- How much money do I get and when? Compare to risk free rates (Bonds)</li>
	<li>Don’t pay more than what you get back in value
	<ul>
	<li>Ask, what do I think the value of this business is, and what is the price? Discount the payoff to present value as well</li>
	<li>Consider business+Management, but realize that no amount of good management can help a bad business</li>
	<li>Need intelligence, energy, and character. 1st 2 without character will kill you</li>
	<li>Heat is literally fast moving particles</li>
	<li>Entropy is not disorder, but the measure of physical energy flow from being concentrated to being spread out<br/>
	<br/></li>
	</ul>
	</li>
	</ul>
	</li>
	</ul>
	</li>
	<li><b>Simplification</b>
	<ul>
	<li><b>Clear tough-minded people are the most simple</b></li>
	<li>Simplify the way we do things
	<ul>
	<li>Turn complicated problems into simpler ones. Put down on paper the key concepts and their relationships. First dispose of the easy decisions</li>
	<li><b>Be problem-oriented, not method oriented</b></li>
	<li>Make fewer and better decisions</li>
	<li>Remove the folly, the unessential. Know what you won’t do. <u>There are often just a few actions that produce most of what we are trying to achieve.</u></li>
	<li><b>People calculate too much and think too little</b></li>
	</ul>
	</li>
	<li>Avoid certain things
	<ul>
	<li>Know the kinds of people and situations to avoid. Pick no brainers</li>
	</ul>
	</li>
	<li>Focus leads to understanding and efficiency</li>
	<li>Focus on what you can know and what makes a difference</li>
	<li>Ask the right questions
	<ul>
	<li>What does it mean? What is the simplest example? What is the number one question? How can I tell if the answer is right? Can I come up with an example that makes it clear what the problem is?</li>
	<li><i>“The formulation of a problem is often more essential than its solution, which may be merely a matter of mathematical or experimental skill”-</i> Albert Einstein</li>
	</ul>
	</li>
	<li>Patience
	<ul>
	<li>Best thinking is often done with no stress, time limits, threats, or judging</li>
	<li>Gather the facts, and allow your subconscious to operate and make connections<br/>
	<br/></li>
	</ul>
	</li>
	</ul>
	</li>
	<li><b>Rules and Filters</b>
	<ul>
	<li>Establish “What to do” and “What to avoid” rules
	<ul>
	<li>Any time you’re offered anything with a big commission and 200 page prospectus, decline it</li>
	<li>Apply yourself industriously to the business at hand, and don’t get distracted by a foolish project of suddenly growing rich. Have patience and do work that matters</li>
	<li>Walk away from anything I don’t understand, can’t quantify, or with people I don’t trust.</li>
	<li>Only take risks you can evaluate and yield a positive expected value</li>
	<li>Make sure no single risk carries a loss you can’t withstand</li>
	<li><b>Avoid moral risk at all costs.</b> Don’t work with bad people or bad things</li>
	</ul>
	</li>
	<li>Filters
	<ul>
	<li>Consider the most critical factors for getting what you want/aim to avoid. Rank by importance and evaluate according to that</li>
	<li><i>I</i><i>nvesting:</i> Can I understand it? Does it look like it has a competitive advantage? Is the management composed of able and honest people? Is the price right?</li>
	<li>Avoid low quality people: Do they seem like a good person? Do they have a desire to grow and better understand the world? </li>
	</ul>
	</li>
	<li>Elimination
	<ul>
	<li>Eliminate situations that may cause sorrow/regrets, what is not important or knowable, what can’t happen, what can’t be predicted or explained, what can’t be tested, what is already disproved, and wrong assumptions.</li>
	</ul>
	</li>
	<li>Checklist procedures
	<ul>
	<li>Use models and run down them like a checklist</li>
	<li>Different issues need different checklists</li>
	<li>Checklist must include critical items for safety and avoiding accidents so we needn’t rely on memory.</li>
	<li>Should be readily usable and easy to use. Should agree with reality</li>
	<li><u>Always think as well.</u> Don’t automate thinking<br/>
	<br/></li>
	</ul>
	</li>
	</ul>
	</li>
	<li><b>Goals</b>
	<ul>
	<li>Know what you want to accomplish and why— can use what you don’t want as a baseline. Reason what you want, the right goal, and how to get it</li>
	<li>Should be: Clearly defined, focused on results, realistic and logical (neither too easy or difficult), measurable, tailored to our needs, subject to change (based on current objective)</li>
	<li>Questions: What result do I want? What causes that? What factors have a major impact? What single factor has the most? Do I have the variables? What’s the best way to achieve it?<br/>
	<br/></li>
	</ul>
	</li>
	<li><b>Alternatives</b>
	<ul>
	<li>Opportunity cost
	<ul>
	<li>Weigh costs by the value of money, time, or effort on the <u>best</u> alternative</li>
	<li>When we decide whether to change something, we should measure against the best of what we already have. Make a list of pros and cons and weight them against each other</li>
	<li>Education, spouse, friends, and careers matter<br/>
	<br/></li>
	</ul>
	</li>
	</ul>
	</li>
	<li><b>Consequences</b>
	<ul>
	<li>Always ask- “<i>And then what?” </i></li>
	<li>When evaluating consequences:
	<ol>
	<li>Pay attention to the whole system. Direct and indirect effects.</li>
	<li>Consequences have implications and more consequences.  Look out for unwanted consequences</li>
	<li>Consider the effects of feedback, time, scale, repetition, critical thresholds and limits</li>
	<li>Different alternatives have different consequences in terms of cost and benefits. Estimate the net benefits over time relative to what we have<br/>
	<br/></li>
	</ol>
	</li>
	</ul>
	</li>
	<li><b>Quantification</b>
	<ul>
	<li>How much capital is required to generate a dollar of cashflow?</li>
	<li>Estimated rate of return— look at current valuation, current revenues and estimated growth rate. Estimate future value based on standard P/E ratio, discount to present value and look at annual return<br/>
	<br/></li>
	</ul>
	</li>
	<li><b>Evidence</b>
	<ul>
	<li>Comes from facts, observations, experiences, comparisons, and experiments</li>
	<li>The methods of science
	<ul>
	<li>Scientific Prowess:</li>
	<li><i>Problem of observation:</i> We try to figure something out. We wonder what happens and why</li>
	<li><i>Hypothesize why:</i> Try to find a possible solution or an explanation that can be prove or disproved by testing. Our guess must be measurable, and agree with nature and proven evidence</li>
	<li><i>Predict consequences:</i> See what would be implied if your guess was right</li>
	<li><i>Test:</i> Compare implied observations with experiment</li>
	<li>Must have observations combined with a theory- it’s the only way to test</li>
	<li>If two explanations make the same predictions, prefer the one with the least unproven assumptions</li>
	</ul>
	</li>
	<li>Finding evidence from the past
	<ul>
	<li>Ask, will the past/present behavior continue? For how long? Why did it happen in the past/now? How did it happen? What were key factors? How predictive is the past of the future? What is necessary to make the past continue? How certain am I? What are the consequences of being wrong?</li>
	</ul>
	</li>
	<li>Falsify and disprove
	<ul>
	<li>Evidence in favor supports, evidence against disproves<br/>
	<br/></li>
	</ul>
	</li>
	</ul>
	</li>
	<li><b>Backward Thinking</b>
	<ul>
	<li>Avoid what causes the <i>opposite</i> of what you want to achieve. Ask- what can I do to get the opposite of the desired outcome? Don’t do that.</li>
	<li>End focusing on what you want to achieve</li>
	<li>Study errors and their consequences. What were the mistakes, why’d they happen, how can we avoid them.</li>
	<li>Assume you’ve already achieved your goal and ask- What was the purpose? Was this what I wanted? What is needed to achieve this?</li>
	<li>When you think you have the right judgment, consider what would cause the opposite of the desired outcome.</li>
	<li>Practice zero base thinking- if we weren’t doing what we already are, what would be optimal?<br/>
	<br/></li>
	</ul>
	</li>
	<li><b>Risk</b>
	<ul>
	<li>Consider the consequences of being wrong</li>
	<li>Build in a margin of safety</li>
	<li>Do your best. Don’t lie. Do what you say you’re going to. Ge there early, apologize w/o excuses.</li>
	<li>Don’t leave people hanging. make up your mind quickly, especially if you’re going to say no<br/>
