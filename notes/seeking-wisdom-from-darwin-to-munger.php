	<div><b>When Analyzing mistakes:</b></div>
	<ul>
	<li>What was my original reason for doing this? What did I know and what did I assume? What were my alternatives?</li>
	<li>How did reality work relative to my guess? What worked and what didn’t?</li>
	<li>Given available info, should I have been able to predict the outcome?</li>
	<li>What worked well? What should I do differently? What did I fail to do? What did I miss? What must I learn? What must I stop doing? </li>
	<li><b>Analyze your failures as much as, if not more than, your successes.</b></li>
	<li>Use pre-mortem, ask how you could fail and what could cause it?</li>
	</ul>
