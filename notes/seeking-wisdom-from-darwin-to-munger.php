	<div><span style="color: rgb(255, 38, 0);"><b><span style="font-size: 18px;">The Physics and Mathematics of Misjudgments</span></b></span></div>
	<div><b><span style="font-size: 18px;"><span style="color: rgb(255, 38, 0);"><br/></span></span></b></div>
	<div>Now that you know the psychological side of error, it’s important to know the mathematical side. Learn to use mental models appropriately and well- you’re thinking will be substantially better. </div>
	<div><br/></div>
	<div>As before, it involves a list of potential misjudgments and examples</div>
	<div><b><span style="font-size: 18px;"><br/></span></b></div>
	<ol>
	<li><span style="color: rgb(4, 51, 255);"><b>Systems Thinking</b></span>
	<ul>
	<li>We fail to consider that actions have intended and unintended consequences. This includes failing to consider secondary and higher-order consequences and inevitable implications
	<ul>
	<li>The consequences of actions have consequences, whether or not this is a shift in incentives, other parties feeling hurt, etc.</li>
	<li><u>Focus on the likely consequences of your actions, not intentions.</u></li>
	</ul>
	</li>
	<li>We fail to consider the entire system in which actions and reactions take place. Includes factors that make up the system, their relationships, and effects of changes on system outcome.
	<ul>
	<li>Don’t micro-optimize, optimize for the intended outcome</li>
	<li>Ask: What key factors influence the outcome and how do they interact? What other things may change as a consequence? Given these, what likely and unlikely consequences will the proposed action have? What will the net result be?</li>
	</ul>
	</li>
	<li>Failing to consider the likely reaction of others (and that what is best may be dependent on that)</li>
	<li>Failing to consider implications of winning a bid (overestimating the value and paying too much)</li>
	<li>Overestimating predictive ability or using unknowable factors in making predictions
	<ul>
	<li>Models are good for thought exercises, but not precise mathematical indicators<br/>
	<br/></li>
	</ul>
	</li>
	</ul>
	</li>
	<li><b><span style="color: rgb(4, 51, 255);">Scale and Limits</span><br/></b>
	<ul>
	<li>Failing to consider that changes in size or time influence, form, function, and behavior
	<ul>
	<li>Relationship between surface area, volume, etc. as you scale. Volume grows faster than surface area as you grow. As you divide, surface area per unit volume grows. (When hiring someone new, payments also grow over time)</li>
	</ul>
	</li>
	<li>Failing to consider breakpoint, critical thresholds and limits
	<ul>
	<li><b>Multiple Effects which can come from scale:</b> Critical mass/threshold, Economies of scale, Natural tendency to Monopoly (newspapers, for example), per-unit cost increase, overcomplicating, bureaucracy and inefficiency</li>
	</ul>
	</li>
	<li>Failing to consider constraints- a system’s performance is constrained by its weakest link
	<ul>
	<li>Find primary chokepoints in desired outcomes and work on them, don’t just optimize one variable without looking at the whole mechanism<br/>
	<br/></li>
	</ul>
	</li>
	</ul>
	</li>
	<li><span style="color: rgb(4, 51, 255);"><b>Causes</b></span>
	<ul>
	<li>Not understanding what causes desired results
	<ul>
	<li>Start from the desired result and reason backwards from there (What causes it, what factors are at play?)</li>
	</ul>
	</li>
	<li>Believing cause resembles its effect- a big effect MUST have a big or complicated cause</li>
	<li>Underestimating the influence of randomness in bad or good outcomes
	<ul>
	<li>Accept when a random/unforeseeable event caused an issue</li>
	</ul>
	</li>
	<li>Mistaking an effect for its cause. Failing to consider that many effects may originate from one common root cause.
	<ul>
	<li>Don’t just patch problems you encounter, look into why they’re happening</li>
	</ul>
	</li>
	<li>Attributing outcome to a single cause when there are multiple
	<ul>
	<li>Many factors confounding will often cause issues</li>
	</ul>
	</li>
	<li>Mistaking correlation for cause
	<ul>
	<li>Look at standard frequencies, and then compare with that frequency given a specific factor</li>
	</ul>
	</li>
	<li>Failing to consider that an outcome may be consistent with alternative explanations</li>
	<li>Drawing conclusions about causes from selective data. Includes identifying the wrong cause based on a single observed effect, also failing to consider missing info or evidence.
	<ul>
	<li>When thinking you’ve found a solution, compare it across multiple cases (experimental group w/variable and other w/o.)</li>
	<li>We often fail to think of alternative possibilities out of the norm.</li>
	</ul>
	</li>
	<li>Not comparing the difference in conditions, behavior, and factors b/w negative and positive outcomes in similar situations when explaining outcomes<br/>
	<br/></li>
	</ul>
	</li>
	<li><span style="color: rgb(4, 51, 255);"><b>Numbers and their meaning</b></span>
	<ul>
	<li>Looking at isolated numbers- failing to consider relationships and magnitudes. Includes not using basic math to quantify and not differentiating between relative and absolute risk
	<ul>
	<li>When told profits were “x” or this is a deal because it’s cheap, always compare to relative indicator— How much was spent to get those profits? How valuable is X relative to cost?</li>
	<li>Statistics can lie— look at both relative AND absolute magnitude</li>
	</ul>
	</li>
	<li>Underestimating effect of exponential growth
	<ul>
	<li>They are absurd. YUGE I tell ya'</li>
	</ul>
	</li>
	<li>Underestimating the time value of money
	<ul>
	<li>Money now is worth more than later BECAUSE of compound interest. Know how to discount to present value based on anticipated returns<br/>
	<br/></li>
	</ul>
	</li>
	</ul>
	</li>
	<li><span style="color: rgb(4, 51, 255);"><b>Probabilities and number of possible outcomes</b></span>
	<ul>
	<li>Underestimating risk exposure when relative frequency and magnitude of consequences is unknown or changes often
	<ul>
	<li>Look at both averages AND trends.</li>
	<li>Use relevant comparison groups using similar factors to get the best info</li>
	<li>Understand when past experience is not a good indicator for the future</li>
	<li>Consider the worst case and it’s likelihood.</li>
	<li>Build based both on experience and potential exposure</li>
	<li>Understand forces that make events more or less likely — The more opportunities an event has to happen relative to what can happen, the more likely it is to occur.</li>
	</ul>
	</li>
	<li>Underestimating # of possible outcomes for unwanted events. Includes underestimating likelihood and severity of rare or extreme events.
	<ul>
	<li>The more potential outcomes there are, the less likely an individual one is</li>
	<li>Things are not likely if they demand a high number of unlikely and independent events, and an unwanted event is likely to happen</li>
	<li>Eliminating risk is preferable to finding where it lies. Can reduce risk by increasing the number of wanted possible outcome, reducing the number of unwanted, reducing magnitude of consequences and avoiding certain situations</li>
	</ul>
	</li>
	<li>Overestimating the chance of rare but widely publicized+emotional events and underestimating the chance of common but less publicized events
	<ul>
	<li>How likely is the event, how serious the consequences? How often does it NOT happen.</li>
	<li>We tend to overestimate the probability of low likelihood but high payoff bets</li>
	</ul>
	</li>
	<li>Failing to consider probabilities and consequences (expected value)
	<ul>
	<li>Always consider expected costs vs. expected gains</li>
	</ul>
	</li>
	<li>Believing events where chance plays a role are self-correcting- that previous outcomes of independent events have predictive value for the future
	<ul>
	<li>Understand when probabilities exist independent of recent events</li>
	</ul>
	</li>
	<li>Believing one can control the outcome of events where chance is involved</li>
	<li>Judging financial decisions by evaluating gains and losses instead of final state of wealth and personal value
	<ul>
	<li>Always compare potential losses and gains to your current situation, and consider expected utility</li>
	<li>Will the loss be devastating?</li>
	</ul>
	</li>
	<li>Failing to consider the consequences of being wrong<br/>
	<br/></li>
	</ul>
	</li>
	<li><span style="color: rgb(4, 51, 255);"><b>Scenarios</b></span>
	<ul>
	<li>Overestimating where all of a series of steps must be achieved for a wanted outcome. Underestimating opportunities for failure and what normally happens in similar situations
	<ul>
	<li>Don’t focus on this specific cases but the base rate for these cases in general</li>
	<li>The more independent steps necessary for something to succeed, the more opportunity for failure.</li>
	<li>The weakest link sets the upper limit for the whole chain</li>
	<li>Should only take a bet if p*payoff&gt;cost</li>
	<li>Be willing to take multiple risky bets, but don’t put too much in one that if fails (6 startup with a success rate of 40%- there is a 99.4% chance that one of them succeeds)</li>
	<li>Everyone is incentived to overestimate their results and success</li>
	</ul>
	</li>
	<li>Underestimating the probability of systems failure- scenarios composed of many parts where system failure can happen one way or another. Includes failing to consider that time horizon changes probabilities. Also assuming independence when it is not present and/or assuming all events are equally likely.
	<ul>
	<li>2,000 parts w/ a 99.9% success rate, there is only an 86% chance of success w/o catastrophe. Don’t underestimate probability of failure</li>
	<li>Even w/backup systems. understand if systems relate or domain effect is possible</li>
	<li>Low probability catostrophes should still be prepared for- they WILL happen (i.e. a tremendous natural disaster)</li>
	<li>Also add in human error- even when tech doesn’t fail, people will.</li>
	</ul>
	</li>
	<li>Not adding a factor of safety for known and unknown risks. Size of factor depends on consequences of failure, how well risks are understood, system characteristics and degree of control
	<ul>
	<li>Add a safety factor to further reduce the probability of catastrophe. </li>
	<li>Consider accidents and patterns, breakpoints, and contingency plans.<br/>
	<br/></li>
	</ul>
	</li>
	</ul>
	</li>
	<li><span style="color: rgb(4, 51, 255);"><b>Coincidences and Miracles</b></span>
	<ul>
	<li>Underestimating that surprises and improbable events happen, somewhere, sometime, to someone, if they have enough opportunities (large enough time or size) to happen.
	<ul>
	<li>We underestimate the #of opportunities for unlikely events to happen. Low probability events w/millions of opportunities become likely</li>
	</ul>
	</li>
	<li>Looking for meaning, searching for causes and making up patterns for chance events, especially event with emotional implications.
	<ul>
	<li>We will search for meaning and interpretation even in perfectly random events</li>
	<li>Low probability events WILL happen in our life. Doesn’t necessarily mean it is probabilistically high.</li>
	</ul>
	</li>
	<li>Failing to consider cases involving the absence of cause and effect 
	<ul>
	<li>When a chance event happens, think about all the time happens. Ex. X and Y happen. Also think about when there was X and ~Y, ~X and Y, and ~X and ~Y.</li>
	<li>When someone ‘predicts the future” understand it is easy when you make predictions all day and only highlight when you’re right. Predictive accuracy is still low</li>
	<li>Predictions are easy when they’re overly general.</li>
	<li>“There’s no evidence that ghosts don’t exist”. Doesn’t mean they exist. <u>Truth is determined by the evidence in support of something, not the lack of evidence against it.</u></li>
	<li>If the opposite of the statement is more likely than the statement, the statement probably isn’t true. “Alice witnessed a miracle” vs “Alice saw an illusion”. <br/>
	<br/></li>
	</ul>
	</li>
	</ul>
	</li>
	<li><span style="color: rgb(4, 51, 255);"><b>Reliability of case evidence</b></span>
	<ul>
	<li>Overweighing case evidence and under-weighing the underlying probability. Includes failing to consider the probability of a random match, false positive and false negative. Also failing to consider a relevant comparison population that bears the characteristic we are seeking
	<ul>
	<li>Consider underlying probability- “<i>So and so stole X, what’s the probability they never did before?”</i> What’s the probability that someone who steals is a repeat offender?</li>
	<li>John says the cab involved in an accident was green. To calculate, use bayesian probability. What % of cabs are green? Other colors? What % of the time does john identify right? 4 outcomes: Green and correct, Green and wrong, other color and says it’s green, other color and says it’s another color. What’s the probability john said it was green and correct relative to probability john said it was green?</li>
	<li>Consider past probabilities</li>
	<li>Must consider probability of a random match</li>
	<li>Consider false positives and false negatives</li>
	<li>Consider probability of X and Y, ~X and Y, X and ~Y, ~X and ~Y<br/>
	<br/></li>
	</ul>
	</li>
	</ul>
	</li>
	<li><span style="color: rgb(4, 51, 255);"><b>Misrepresentative evidence</b></span>
	<ul>
	<li>Failing to consider the changes in factors, context, or conditions when using past evidence to predict the future. Includes not searching for explanations to why past outcome happened, what is required to make it continue, and what forces can change it.
	<ul>
	<li>Statistics are a record of the past, only limited in predictive ability <u>big difference in using stats to track improvement vs. using them to predict the future</u></li>
	<li>Should consider prior circumstances AND how they’ve changed</li>
	</ul>
	</li>
	<li>Overestimating evidence from a single case of small or unrepresentative samples
	<ul>
	<li>Statistics alone aren’t valuable, must consider <i>sample size, relative effects AND absolute effects.<b> </b></i></li>
	<li><b>Need a representative dataset-</b><u>better to be roughly correct than precisely wrong</u></li>
	</ul>
	</li>
	<li>Underestimating the influence of chance in performance
	<ul>
	<li>Sometimes track record is a bad indicator- with a large enough sample,  we only see the few winners and not the tremendous volume of losers, shows a low probability of winning</li>
	</ul>
	</li>
	<li>Only seeing positive outcome- paying little attention to negative outcomes and prior probabilities
	<ul>
	<li><u>Survival bias:</u> We only consider the survivors, not the failures as well. Only look at traits of those who win, don’t consider if the failures also have those traits “<i>Focus is the key to success</i>.” How many failures ALSO had great focus?</li>
	<li>Consider probability that claims are correct, given your results</li>
	</ul>
	</li>
	<li>Failing to consider variability of outcomes and their frequency
	<ul>
	<li>When evaluating likely outcomes/truth, consider entire distribution. Average, Variability, and probability of an extreme outcome. Are outliers skewing your data?</li>
	</ul>
	</li>
	<li>Failing to consider regression- in any series of events where chance is involved, unique outcomes tend to regress back to the average</li>
	</ul>
	</li>
	</ol>
