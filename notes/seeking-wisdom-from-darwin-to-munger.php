<h1>
	Seeking Wisdom: From Darwin to Munger
</h1>

<p>Seeking Wisdom is by far one of the greatest books I've ever read. It takes a wealth of research on psychological bias and poor thinking and gathers it in one place- in a highly actionable format to boot. Buy a copy to read and consult it regularly. <br><br>The first half of the book is based on the Psychology of Misjudgement, while the second is focused on more Math and Science based errors.</p><br>

</div>

<div class="notes">

	<div><span style="font-size: 18px;"><b>28 Reasons for misjudgments and mistakes</b></span></div>
	<div><b><span style="font-size: 18px;"><br/></span></b></div>
	<div>Perhaps the most important first step to thinking well is simply to <i>avoid thinking badly.</i> You actually have to get to 0 before you can start building better models on top.</div>
	<div><br/></div>
	<div>As humans, much of the way we think comes from evolutionarily advantageous traits that aren’t so valuable in our current climate. That means many of our “gut reactions” are actually useless.</div>
	<div><br/></div>
	<div>Here’s a list of natural human biases that can often lead us astray, along with examples to demonstrate how.</div>
	<div><b><br/></b></div>
	<ol>
	<li><span style="color: rgb(148, 33, 146);"><u>Bias from mere association</u><i>:</i></span> We automatically connect a stimulus with pain or pleasure, leading us to like or dislike something merely because it is associated with something good or bad.<br/>
	<br/>
	Think in terms of people- if a person you don’t know is friends with someone you dislike, you think less of them despite your complete lack of knowledge.<br/>
	 <br/>
	This includes bias from <i>Persian Messenger Syndrome</i>- not wanting to be the carrier of bad news<br/>
	<br/>
	<ul>
	<li>If something is associated with pleasurable things, you’re more likely to buy it (for expensive things) or make a bad decision (with people)</li>
	<li>To avoid this bias, <i>keep in mind:</i> Evaluate things, situation, and people on their own merits<i>.</i> Disassociate from irrelevant factors and weight those factors appropriately</li>
	<li>Individuals can’t be judged as good or bad simply because we associate them with something negative or positive</li>
	<li>Encourage people to tell you bad news immediately- they’re natural inclination is to avoid it, despite what is best for the situation</li>
	<li>Remove past experiences of pleasure or pain with present experiences- judge based on the present circumstance</li>
	<li>Create a negative emotion if you want to end a certain behavior/habit (create a positive emotion to encourage it)<i><br/>
	<br/></i></li>
	</ul>
	</li>
	<li><span style="color: rgb(148, 33, 146);"><u>Underestimating the power of rewards and punishment:</u> People repeat actions that result in rewards and avoid those they are punished for<br/></span><br/>
	<ul>
	<li>At first, reward/punishment is most effective when awarded immediately and consistently (every time)</li>
	<li>Once a behavior is learned, variable reward is best</li>
	<li>We tend to be over-optimistic risk-takers after success and overly risk-averse after failure, despite what is best in that situation.</li>
	<li>We prefer multiple good events over one large one ($50 twice vs $100 once). We also prefer loss before gain (we’d prefer to lose $100 and then gain $50 over gaining $50 and then losing $100).</li>
	<li>Praise&gt;Punishment when it comes to changing behavior. Praise good behaviors more often than chastising bad ones.</li>
	<li>Understand that punishment is to discourage that behavior, both for that individual and others. If you’re going to do it, make an example of it- it’ll be more impactful for the individual and speak to others. Save this for a deeper level of bad (i.e. stealing).</li>
	<li>Don’t over learn from your own good or bad experiences- conditions matter a lot. Always reassess present circumstance.</li>
	<li><b>Separate between skill and chance</b>- understand what each contributed. We often claim skill when positive outcomes result and chance when negative outcomes do. This is injurious to potential learnings.</li>
	<li>Make incentives appropriate- tie them to performance and results. Make people share the downside and upside.</li>
	<li>DON’T LET MONEY BE THE ONLY MOTIVATION. If someone already likes a task, it may make it feel like work. Award achievement and results, not simply doing</li>
	<li>Never let it pay for someone to act in a way you don’t want. <b>It should never payoff to take shortcuts or cheat the system.</b> This takes some thought, but will save you a lot in the long run.<br/>
	<br/></li>
	</ul>
	</li>
	<li><u><span style="color: rgb(148, 33, 146);">Underestimating bias from own self-interest and incentives</span><br/>
	<br/></u>
	<ul>
	<li>Incentives for decision makers determine their behavior. <i>ex:</i> When the first dead sea scrolls were found, they offered rewards for each fragment. As a result, finders would rip up whole scrolls to increase their reward.</li>
	<li>Commissioned salesman (consultants, lawyers) have a tendency to serve the transaction, not the truth. They are incentivized to do so</li>
	<li>Don’t trust projections made by the interested party. They are often overly optimistic to the point of lying, and the party actually believes it</li>
	<li>Look at past projections and how they compared to actual performance- this will give you an idea of their bias and error in projections</li>
	<li>Most corporate environments serve to confirm beliefs, biases, and opinions of the CEO, not find and create truth. It is in the best interest of these parties to make the CEO like them</li>
	<li>When you aim to persuade, think of <i>interest,</i> not <i>reason.</i> What does this person value and want? Remember, people weigh the fear of loss more than the excitement to win, so also highlight what they stand to lose if they don’t make a decision you want them to<br/>
	<br/></li>
	</ul>
	</li>
	<li><span style="color: rgb(148, 33, 146);"><u>Self-serving bias:</u> We have an overly positive view of our abilities and future<br/></span><br/>
	<ul>
	<li>We overestimate our ability to predict the future and believe things will work out better than they will. We all think of ourselves as the center of the universe.</li>
	<li>When successful, we credit ourselves. When failing, we credit chance.</li>
	<li>We tend to use the tools at hand/that we’re good with (both physical and mental models) to solve problems, even if there are easier and better ways.</li>
	<li>Be aware of how much you don’t know— Try to be consistently not stupid rather than consistently intelligent.</li>
	<li><b>Don’t do anything you don’t think you understand</b> (understand it first)</li>
	<li>Ask: How can I be wrong? Who can tell me? How will I fix it? This will prepare you for realistic issues in the future</li>
	<li>Accrue and use ideas from multiple disciplines— this will allow you to have a full toolkit and find better solutions for different problems</li>
	<li>Use individual track records to determine potential for success, not personality/behavior<br/>
	<br/></li>
	</ul>
	</li>
	<li><u style="color: rgb(148, 33, 146);">Self-deception and denial:</u><font color="#942192"> We distort reality to reduce pain or increase pleasure, this includes wishful thinking.</font><br/>
	<br/>
	<ul>
	<li>“You must not fool yourself- and you are the easiest person to fool"</li>
	<li>“…it is the habit of mankind to entrust to careless hope what they long for, and to use sovereign reason to thrust aside what they do not desire."</li>
	<li>Be real with yourself regarding what you want to happen, it better prepares you to <i>make</i> it happen.</li>
	<li>We believe something is true because it <i>sounds</i> believable or we <i>want</i> to believe it, not because there’s a high likelihood it will be.</li>
	<li>True bad news is better than false good news. We must weigh costs of denial vs. facing reality.<br/>
	<br/></li>
	</ul>
	</li>
	<li><span style="color: rgb(148, 33, 146);"><u>Bias from consistency tendency:</u></span> Humans feel the need to stay consistent with prior commitments despite them being against our best interest and disconfirming evidence. <i>Includes confirmation bias (sunk cost fallacy as well)</i>- looking for evidence that confirms our action and beliefs<br/>
	<br/>
	<ul>
	<li>The more time and effort we put into something, the more committed we feel to it. This comes from our fear of loss- it's harder to justify losing the time we put in without results than to argue for low payout of continuing</li>
	<li>“What the human being is best at doing is interpreting all new information so that their prior conclusions remain intact."</li>
	<li>We are more committed to public, effortful, and voluntary decisions— it’s harder for us to walk away from them, even if it’s best</li>
	<li>Respond to each task as best you can, don’t try and project far into the future (you’ll fail at doing so if you do)</li>
	<li>Never underestimate the power of ideology- it can justify nearly any bad decision, including violence or war</li>
	<li>When you realize you’re wrong, admit it and actively work to change it- this is the core of all improvement</li>
	<li>Search for evidence that DISCONFIRMS assumptions and beliefs- this is how science works, and they’ve done pretty well. You can always find weak evidence to help confirm, but it only takes one piece of evidence that is truly disconfirming to make a 100% better decision</li>
	<li>Judge your work as if it is someone else’s. Listen to it through a mirror- if someone else was saying this, how you react?<br/>
	<br/></li>
	</ul>
	</li>
	<li><span style="color: rgb(148, 33, 146);"><u>Bias from deprival syndrome</u>:</span> We strongly react when something we like and have (or almost have) is (or threatens to be) taken away- The <i>Endowment Effect</i>. Includes overvaluing what we can’t have but is (or threatens to be) less available. <br/>
	<br/>
	<ul>
	<li>“Failure is embarrassing and may get me fired.” Causes people to be more risk averse at work, don’t want to lose their job</li>
	<li>Frequent near misses make you feel as if you have ownership and continue to make conservative risk-averse decisions. You don’t want to risk losing badly, even if the payout is far larger.</li>
	<li>Keep in mind- <u>Why do I want this?</u> Is it for emotional or rational reasons?<br/>
	<br/>
	There’s <i>a lot</i> more about this in <i>Thinking, Fast and Slow</i> and <i>Predictably Irrational</i>, so my notes on it here a pretty crappy.<br/>
	<br/></li>
	</ul>
	</li>
	<li><span style="color: rgb(148, 33, 146);"><u>Status quo bias and do nothing syndrome:</u> We prefer to keep things the way they are- we minimize effort and stick to default options<br/></span><br/>
	<ul>
	<li>The more emotional a decision, the more we lean on default behaviors (even if switching costs are low).</li>
	<li>We feel worse when we fail from action than when we fail from inaction (think trying to find love, etc.), but at the end of our lives most humans have far more regret stemming from inaction. Play the long game</li>
	<li>Deciding to do nothing is also an action, and the cost could (and probably is) far greater than deciding to do something</li>
	<li>Once you know, do it: “Perhaps the most valuable result of all education is the ability to make yourself do the things you have to do when it ought to be done whether you like it or not."<br/>
	<br/></li>
	</ul>
	</li>
	<li><span style="color: rgb(148, 33, 146);"><u>Impatience:</u> overvaluing the present relevant to the future<br/></span><br/>
	<ul>
	<li>Make sure to weight short term gain/loss vs. long term gain/loss</li>
	<li>Tied to the above, we prefer to be comfortable in the present to maintain stars quo over taking the hard path that will payoff big time in the future.<br/>
	<br/></li>
	</ul>
	</li>
	<li><span style="color: rgb(148, 33, 146);"><u>Bias from envy and jealousy<br/>
	<br/></u></span>
	<ul>
	<li>“Envy is pain at the good fortune of others."</li>
	<li>We are jealous when we perceive a potential threat to a valued relationship- even if that threat is a benevolent new friend with the other person</li>
	<li>Don’t compare yourself to others. Achieve your own goals and be happy. </li>
	<li>We tend to compare ourselves to similar others, it is where most pain comes from. Don’t derive your value from comparison, derive it from yourself<br/>
	<br/></li>
	</ul>
	</li>
	<li><span style="color: rgb(148, 33, 146);"><u>Distortion by contrast comparison:</u></span> We judge and perceive the absolute magnitude of something based on its difference relative to something else presented closely in time or space, etc. Also underestimating the consequences of gradual change.<br/>
	<br/>
	<ul>
	<li>We determine fairness based on what we are used to, not necessarily on what is truly fair in a situation</li>
	<li>We can often miss small gradual changes (say, to bad behavior) because they must change over a certain threshold of difference for us to notice<br/>
	<br/></li>
	</ul>
	</li>
	<li><span style="color: rgb(148, 33, 146);"><u>Bias from Anchoring:</u></span> over-weighing initial information as a reference point<br/>
	<br/>
	<ul>
	<li>Consider choices from a 0 base level— remember what you want to achieve<br/>
	<br/></li>
	</ul>
	</li>
	<li><u><span style="color: rgb(148, 33, 146);">Over influence by vivid and most recent information</span><br/>
	<br/></u>
	<ul>
	<li>Vivid, emotional, and entertaining stories are often more convincing and memorable than true and powerful numbers, etc.</li>
	<li>Accurate info&gt;vivid info for making decisions, while vivid info is better for convincing<br/>
	<br/></li>
	</ul>
	</li>
	<li><span style="color: rgb(148, 33, 146);"><u>Omission and abstract blindness:</u></span> We only see stimuli we encounter or grabs our attention, neglecting important missing information or the abstract.<br/>
	<br/>
	<ul>
	<li>We overweigh personal experiences and tangible things over the abstract. We often place too much weight on specific events and not other possibilities</li>
	<li>We see what is reported as having happened, not what didn’t happen (everyone who <i>lost</i> the lottery), or other opportunities (results that could have happened if $$$ was allocated differently)</li>
	<li>When deciding to get or do we compare positive features, when rejecting we compare negative features. Compare both no matter what<br/>
	<br/></li>
	</ul>
	</li>
	<li><span style="color: rgb(148, 33, 146);"><u>Bias from reciprocation tendency:</u></span> We are compelled to repay in kind what others have done for us like favors, concessions, information, and attitudes<br/>
	<br/>
	<ul>
	<li>People don’t want to feel indebted- let them return the favor to you</li>
	<li>Gifts are most effective when personal, significant, and unexpected</li>
	<li>Treat others how you would like to be- “How would I want someone to treat me if I did this?"</li>
	<li>Example&gt;Words. Act how you would like others to, don’t dictate how you would like them to act<br/>
	<br/>
	Look at my notes on <i>Influence</i> that goes deep into this, if you’re craving more.<br/>
	<br/></li>
	</ul>
	</li>
	<li><span style="color: rgb(148, 33, 146);"><u>Bias from over-influence by liking</u>:</span> We believe, agree with, and trust people we know and like. Includes bias from over-desire for liking and social acceptance/avoiding social disapproval. Also <u>bias from disliking:</u> disagreeing with people exclusively because we dislike them<br/>
	<br/>
	<ul>
	<li>We like people better who are similar to us, are attractive, popular, cooperative, or who like us.</li>
	<li>We like those who flatter us and give us what we’re missing in life</li>
	<li>People believe you have the same personality as those you associate with. This contributes to credibility and trust. Who you associate with matters</li>
	<li>Asking someone for a favor increases their liking of you- they perceive that you like and trust them</li>
	<li>We behave like others do, and don’t speak the truth or openly question others for fear of the consequences</li>
	<li>Don’t depend on the encouragement or criticism of others</li>
	<li>“<i>Our enemies opinion of us comes closer to the truth than our own."</i></li>
	<li>Social approval is an effective way to influence behavior<br/>
	<br/></li>
	</ul>
	</li>
	<li><span style="color: rgb(148, 33, 146);"><u>Bias from over-influence by social proof:</u> We imitate the behavior/thoughts of many others. Includes crowd folly<br/></span><br/>
	<ul>
	<li>It seems better to fail conventionally (like everyone else has) than succeed unconventionally.</li>
	<li>If no one else seems confused, we are less likely to admit our own confusion</li>
	<li>Crowds are driven by imitation, obedience to authority, and fear of being different</li>
	<li>It is easy to live our own opinion in solitude, but difficult do so in the crowd.</li>
	<li>Disregard what others are doing and think for yourself.  You are not right or wrong based on what others think. Trust your analysis, data, and reasoning.</li>
	<li>When part of a group, encouragement disagreement and alternative points of view- this will help broaden perspective and encourage truth</li>
	<li>Make individuals responsible for their actions, not groups. Groups will fall to groupthink, while individuals being held accountable will push for the best outcome<br/>
	<br/></li>
	</ul>
	</li>
	<li><span style="color: rgb(148, 33, 146);"><u>Bias from over-influence by authority:</u> trusting and obeying a perceived authority or expert<br/></span><br/>
	<ul>
	<li>We are easily influenced and obedient to names, reputation and signals of authority.</li>
	<li>We tend to believe authority/experts more when we don't understand things</li>
	<li>Far worse things have happened in the name of obedience than rebellion— thinks of <i>all</i> of the historical injustices</li>
	<li>Separate between real and false experts. Evaluate based on reasoning, not status. Don’t be afraid to fact check experts<br/>
	<br/></li>
	</ul>
	</li>
	<li><span style="color: rgb(148, 33, 146);"><u>Sense making:</u> Constructing explanations that fit an outcome. This includes drawing conclusion too quick and hindsight bias (thinking events were more predictable than they were)<br/></span><br/>
	<ul>
	<li>Rather than looking into <u>exactly</u> why something happened, thing about general reasons why something happens (ie why products fail). There’s more value in understanding the generals than the specifics</li>
	<li>Rely less on predictions and more on facts/the moment</li>
	<li>We try to find patterns even in perfect randomness. We strongly underestimate chance</li>
	<li>Look for alternative explanations of “Why” after an event- don’t simply rely on your gut reaction<br/>
	<br/></li>
	</ul>
	</li>
	<li><span style="color: rgb(148, 33, 146);"><u>Reason-expecting:</u> We comply with requests merely because we have been given a reason. Includes <i>underestimating</i> the power of giving people reasons<br/></span><br/>
	<ul>
	<li>Whenever making a request of someone, also give a why: They understand it better, they’ll consider it more important and will be more likely to comply</li>
	<li>You think better if you use models that basically ask/answer the questions "why?"</li>
	<li>Ask 3-5 whys. You’ll immediately understand better<br/>
	<br/></li>
	</ul>
	</li>
	<li><span style="color: rgb(148, 33, 146);"><u>Believing first and doubting later:</u> believing what is not true especially when distracted<br/></span><br/>
	<ul>
	<li>We first believe things, and then move to doubt or unverify.</li>
	<li>We are good at identifying the truth and bad at identifying falsehood (it’s too diverse). We <i>know</i> when something is definitely true, but are hesitant to call something out as being false<br/>
	<br/></li>
	</ul>
	</li>
	<li><span style="color: rgb(148, 33, 146);"><u>Memory Limitations:</u> remembering selectively and wrong. Includes influence by suggestions<br/></span><br/>
	<ul>
	<li>We learn better while in a positive mood</li>
	<li>We remember emotional experiences/stories better than non</li>
	<li>7 sins of memory malfunctions/How our memory fails us
	<ul>
	<li>Our memory weakens/lessens over time</li>
	<li>We can be distracted and don’t focus on what we need to remember</li>
	<li>We search for info we desperately want but can’t find it</li>
	<li>We assign memory to the wrong source</li>
	<li>Memories are implanted by leading questions, etc.</li>
	<li>Our present knowledge edits our memories.</li>
	<li>We recall disturbing events we would rather not altogether<br/>
	<br/></li>
	</ul>
	</li>
	</ul>
	</li>
	<li><span style="color: rgb(148, 33, 146);"><u>Do-something syndrome:</u> acting without a sensible reason<br/></span><br/>
	<ul>
	<li>“<i>The question is, What are we busy about?”.</i> Don’t confuse action with results. There is no reason to do a good job with something that shouldn’t be done in the first place.</li>
	<li>Reorganization can create the illusion of progress where there is none</li>
	<li>Become comfortable with inactivity. Don’t do foolish things just to be active</li>
	<li><i>“There’s no use running if you’re on the wrong road.”</i> Focus on what you want to accomplish.</li>
	<li>Doing nothing and simply thinking is better than acting for actions sake<br/>
	<br/></li>
	</ul>
	</li>
	<li><span style="color: rgb(148, 33, 146);"><u>Mental confusion from say-something syndrome:</u> </span><b><span style="color: rgb(148, 33, 146);">feeling a need to say something when we have nothing to say</span><br/>
	<br/></b>
	<ul>
	<li>If you have nothing to say, say nothing at all. You may regret your speech, but rarely your silence.<br/>
	<br/></li>
	</ul>
	</li>
	<li><span style="color: rgb(148, 33, 146);"><u>Emotional arousal:</u> making hast judgements under the influence of intense emotions. Includes exaggerating emotional impact of future events<br/></span><br/>
	<ul>
	<li>We underestimate how we will react when experiencing intense emotions. Emotions come before reason— we must seek to switch to reason as quickly as possible</li>
	<li>Sadness makes us more willing to sell something for less and buy something for more (we feel helpless and and overvalue change to better circumstances)</li>
	<li>When something produces strong emotions, we think consequences will be worse and that the probability of it happening are greater than they actually are (we think good things will be better and more likely to happen than they are, and bad things are worse and more likely to happen).</li>
	<li>Always ask: <u>Is there a rational reason behind my behavior?</u></li>
	<li>A wise man controls his temper. Hold off on important decisions when emotionally aroused. </li>
	<li>Benefits of cooling off force us to think things through<br/>
	<br/></li>
	</ul>
	</li>
	<li><span style="color: rgb(148, 33, 146);"><u>Mental confusion from stress</u></span>
	<ul>
	<li><b>Causes of stress:</b> Too much info, too many choices, lack of predictability/control, social isolation, job status, crisis/catastrophes/fear</li>
	<li>Our stress is more social/psychological, while our ancestors was more physical/demanding</li>
	<li>Emotional stress creates longer recovery times in our bodies and may be more harmful than non-emotional stress (exercise)</li>
	<li>Stress makes us more persuadable</li>
	<li>The more in control we feel, the less stress we receive. Stress does not harm us, but distress does.</li>
	<li>If a problem can be solved, don’t worry- correct it. If it can’t, don’t worry- you can’t do anything anyway. So simply accept what is and isn’t in your control and stop worrying.<br/>
	<br/></li>
	</ul>
	</li>
	<li><span style="color: rgb(148, 33, 146);"><u>Mental confusion from physical or psychological pain:</u></span> the influence of chemicals or diseases (drugs, sickness, etc.)<br/>
	<br/></li>
	<li><span style="color: rgb(148, 33, 146);"><u>Bias from the over-influence</u>: t</span>he combined effect of many psychological tendencies operating together<br/>
	<br/>
	<ul>
	<li>2 or 3 biases working together contribute to extreme irrationality</li>
	<li>Very important to create human conditions that are hard to cheat. Once cheating starts, consistency and social proof will kick in, making it very hard to reverse. “<i>It’s just how everyone did it. Cheating was the norm. Why should we have to stop?"</i><br/>
	<br/></li>
	</ul>
	</li>
	</ol>
	<div><span style="color: rgb(4, 51, 255);"><b>Contextual In</b><b>fluences</b></span></div>
	<div><b><br/></b></div>
	<ul>
	<li>Behavior is influenced by social and situational factors, the structure or description of a problem/choice, and our desires, moods, and expectations.</li>
	<li>We tend to overestimate personal characteristics and motives when we explain the behavior of others</li>
	<li>We ignore chance and poorly designed systems— we place all the weight on people and not the circumstance and systems at play</li>
	<li>We change behavior based on if we’re observed or not</li>
	<li>The less we know, the more we’re influenced by framing (wording/how a question is asked)— people ignore denominators in framing</li>
	<li>Assets/Money are the same regardless of where they come from (You make $1,000 from work vs. gambling, which are you more likely to gamble? You lose $10 vs a $10 ticket, in which are you likely to buy another ticket?)<br/>
	<br/></li>
	<li><b>3 pieces of advice from Munger:</b>
	<ol>
	<li>You will make mistakes. Learn to make <i>fewer</i> mistakes and <i>correct faster</i> when you <i>do</i> make them. It’s actually valuable to <i>make</i> and <i>learn to handle</i> mistakes.</li>
	<li>Evaluate the real <i>interests</i> and <i>probabilities</i> involved, as well as the <i>psychological factors</i> the cause subconscious errors.</li>
